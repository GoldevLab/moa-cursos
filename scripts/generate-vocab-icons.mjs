/** Iconos distintos por opción en la misma ronda (evita duplicados). */
export const getDistinctVocabIconNames = (terms: string[]): VocabIconName[] => {
  const used = new Set<string>();
  return terms.map((term) => {
    const key = term.trim().toLowerCase();
    const candidates = [
      getVocabIconName(term),
      ...(TERM_ICON_ALTS[key] ?? []),
      ...FALLBACK_ICONS,
    ];
    const icon = candidates.find((item) => !used.has(item)) ?? "CircleHelp";
    used.add(icon);
    return icon;
  });
};

