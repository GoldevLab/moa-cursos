const imports = uniqueIcons.map((n) => `Lu${n}`).join(",\n  ");
const switchCases = uniqueIcons
  .map((n) => `    case "${n}":\n      return <Lu${n} class={cls} />;`)
  .join("\n");

const componentTsx = `/** Generado por scripts/generate-vocab-icons.mjs — no editar a mano. */
import { component$ } from "@builder.io/qwik";
import {
  ${imports},
} from "@qwikest/icons/lucide";
import {
  getVocabIconName,
  VOCAB_ICON_COLOR_CLASS,
  type VocabIconName,
} from "~/lib/lesson-vocab-icon-map";

const renderIcon = (name: VocabIconName, cls: string) => {
  switch (name) {
${switchCases}
    default:
      return <LuCircleHelp class={cls} />;
  }
};

export const VocabIcon = component$(
  (props: { term?: string; name?: VocabIconName; class?: string }) => {
    const iconName = props.name ?? getVocabIconName(props.term ?? "");
    const colorKey = props.term?.trim().toLowerCase() ?? "";
    const colorClass = VOCAB_ICON_COLOR_CLASS[colorKey] ?? "text-violet-700";
    const cls = [
      props.class ?? "h-14 w-14",
      colorClass,
    ].join(" ");
    return renderIcon(iconName, cls);
  },
);
`;

writeFileSync(join(root, "src/components/student/vocab-icon.tsx"), componentTsx);

